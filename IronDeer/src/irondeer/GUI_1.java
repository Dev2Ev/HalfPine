    private void toConsole(Object obj){
        String mes="";
        mes+=Calendar.HOUR_OF_DAY+":"+Calendar.MINUTE+":"+Calendar.SECOND;
        mes=mes+" > ";
        try{
            switch(obj.getClass().getName()){
                case "java.awt.event.MouseEvent":
                    mes=mes+"[MouseEvent]";                    
                    break;
                default:
                    mes=mes+"[D]";
                    break;
            }
           if(pressed)
               mes+="Premuto e non click";
        System.out.println(mes);
        }
        catch(Exception e){
            mes+=e.toString();
            System.out.println(mes+"\t"+obj.toString());
        }
    }
